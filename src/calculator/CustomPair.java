package Calculator;

public class CustomPair {
    private String key;
    private String value;
    
    public CustomPair(String k,String v) {
    	this.key = k;
    	this.value = v;
     }

    public String getK() {
    
    return this.key;
    }
    
    public String getV() {
        
        return this.value;
        }
}